#include <iostream>
using namespace std;

int main() {
	int myNum = 15;
	cout << myNum << endl;
	
	int isNum;
	isNum = 16;
	cout << isNum << endl;
	
	int num = 17;
	num = 18;
	cout << num << endl;
	
	int myNumber = 5;				// Integer (whole number without decimals)
	cout << myNumber << endl;
	double myFloatNumber = 5.99;	// Floating point number (with decimals)
	cout << myFloatNumber << endl;
	char myLetter = 'D';			// Character
	cout << myLetter << endl;
	string myText = "Hello";		// String (text)
	cout << myText << endl;
	bool myBoolean = true;			// Boolean (true or false)
	cout << myBoolean << endl;
	myBoolean = false;
	cout << myBoolean << endl;
	
	int myAge = 21;
	cout << "I am " << myAge << " years old.\n";
	
	string name = "Rosendo";
	int age = 21;
	double height = 6.1;
	cout << name << " is " << age << " years old and " << height << " feet tall." << endl;
	
	int x = 5;
	int y = 6;
	int sum = x + y;
	cout << sum;
	
	return 0;
}